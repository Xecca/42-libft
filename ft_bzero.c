#include <unistd.h>

void    bzero(void *s, size_t n)
{
    int i;

    i = 0;
    if (n == 0 || ft_strlen(s) == 0)
        return (0);
    else
        while (s != '\0')
        {
        if (s[i] == '0')
            write(1, s[i++], 1);
        else
            i++;        
        }

}